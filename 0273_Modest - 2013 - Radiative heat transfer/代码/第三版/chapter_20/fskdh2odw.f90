   SUBROUTINE Fskdh2odw(Tg,Tb,cabs,x,gcal)
   IMPLICIT NONE
   INTEGER  :: l,m,n
   DOUBLE PRECISION :: bigp,xi,xis,gcal,Tg,Tb,cabs,x
   DOUBLE PRECISION :: almn(0:3,0:3,0:3),blmn(0:2,0:3,0:3)
   DATA almn/  1.610300D-00, -4.09310D-00,   5.143500D-00, -2.085700D-00,&
              -0.818120D-00, 15.55250D-00, -21.819000D-00,  9.877500D-00,&
               2.600100D-00,-21.20400D-00,  31.082800D-00,-14.279000D-00,&
              -1.317100D-00,  9.65240D-00, -14.474000D-00,  6.674700D-00,&
               0.440187D-00, -0.63348D-00,   0.871627D-00, -0.387980D-00,&
              -0.821640D-00,  5.02390D-00,  -5.981800D-00,  2.635500D-00,&
               1.514900D-00, -7.80320D-00,   9.864200D-00, -4.193100D-00,&
              -0.810230D-00,  3.72700D-00,  -4.874000D-00,  1.986800D-00,&
               0.106647D-00, -0.43116D-00,   0.689598D-00, -0.298310D-00,&
              -0.385730D-00,  1.88650D-00,  -2.971200D-00,  1.283400D-00,&
               0.578351D-00, -2.62180D-00,   4.269800D-00, -1.792900D-00,&
              -0.280140D-00,  1.17850D-00,  -1.956800D-00,  0.787249D-00,&
               8.250270D-03, -3.28556D-02,   6.815630D-02, -3.048150D-02,&
              -3.105780D-02,  0.123369D-0,  -0.261540D-00,  0.117452D-00,&
               4.393190D-02, -0.15792D-00,   0.350948D-00, -0.153080D-00,&
              -2.036990D-02,  6.61142D-02,  -0.152830D-00,  6.340350D-02/
   DATA blmn/  4.720000D-00, -8.54820D-00,   5.239400D-00,&
              -0.849690D-00,  0.312478D-0,  -0.138040D-00,&
              -3.472430D-02,  4.02461D-02,  -5.801040D-02,&
               5.798300D-04,  3.94125D-03,  -5.290170D-03,&
              -8.961500D-00, 16.95470D-00, -10.760000D-00,&
               1.586100D-00, -2.01660D-00,   1.460000D-00,&
               4.347300D-02, -0.67133D-00,   0.633231D-00,&
               2.870670D-03, -7.06830D-02,   6.237100D-02,&
               9.146100D-00,-17.32700D-00,  11.186400D-00,&
              -1.397500D-00,  1.99650D-00,  -1.693500D-00,&
               8.464190D-02,  0.599994D-0,  -0.700540D-00,&
               7.147190D-03,  6.62086D-02,  -6.872940D-02,&
              -3.550400D-00,  6.62400D-00,  -4.305800D-00,&
               0.485392D-00, -0.70710D-00,   0.689109D-00,&
              -6.774560D-02, -0.18179D-00,   0.269308D-00,&
              -5.927260D-03, -2.04694D-02,   2.564110D-02/
   xi=LOG(cabs)
   xis=0d0
   DO l=0,3
     DO m=0,3
       DO n=0,2
         xis=xis+blmn(n,m,l)*(Tb/2500d0)**n*xi**m*x**(l+1)
       ENDDO !n
     ENDDO !m
   ENDDO !l
   bigp=0d0
   DO l=0,3
     DO m=0,3
       DO n=0,3
         bigp=bigp+almn(n,m,l)*(Tg/2500d0)**n*(Tb/2500d0)**m*(xi-xis)**l
       ENDDO !n
     ENDDO !m
   ENDDO !l
   gcal=0.5d0*tanh(bigp)+0.5d0
   RETURN
   END SUBROUTINE Fh2o
