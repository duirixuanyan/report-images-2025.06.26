   SUBROUTINE Fskdco2dw(Tg,Tb,cabs,gcal)
   IMPLICIT NONE
   INTEGER  :: l,m,n
   DOUBLE PRECISION :: bigp,xi,gcal,Tg,Tb,cabs
   DOUBLE PRECISION :: dlmn(0:3,0:3,0:3)
   DATA dlmn/ 2.45702D-00,  -5.45334D-00,   6.53751D-00 ,  -2.52344D-00  ,&
             -4.02320D-00,  15.67297D-00, -24.3247D-00  ,  11.33757D-00  ,&
              7.54549D-00, -23.80230D-00,  39.51896D-00 , -19.1137D-00   ,&
             -3.63104D-00,  11.90780D-00, -20.3606D-00  ,   9.97877D-00  ,&
              7.65678D-02,   2.36184D-00,  -3.95061D-00 ,   2.17482D-00  ,&
              0.2901819D0, -12.00410D-00,  22.44342D-00 , -13.0467D-00   ,&
             -0.64282D-00,  21.50030D-00, -40.8667D-00  ,  23.66762D-00  ,&
              0.3942158D0, -11.58180D-00,  22.05176D-00 , -12.6536D-00   ,&
             -3.30582D-02,   0.4367742D0,  -0.725331D-00,   0.4138566D-00,&
              0.3672993D0,  -3.52466D-00,   6.74885D-00 ,  -3.96295D-00  ,&
             -0.69811D-00,   6.60703D-00, -12.9667D-00  ,   7.58713D-00  ,&
              0.3831158D0,  -3.65683D-00,   7.19415D-00 ,  -4.16496D-00  ,&
             -1.87927D-03,   1.92123D-02,   -3.25863D-02,   1.98493D-02  ,&
              2.85033D-02,  -0.223537D0 ,    0.4402715D0,  -0.26267D-00  ,&
             -5.49594D-02,   0.4370937D0,   -0.881494D0 ,   0.521958D-00 ,&
              3.04198D-02,  -0.247793D0 ,    0.4990777D0,  -0.291566D0/
   xi=LOG(cabs)
   bigp=0d0
   DO l=0,3
     DO m=0,3
       DO n=0,3
         bigp=bigp+dlmn(n,m,l)*(Tg/2500d0)**n*(Tb/2500d0)**m*xi**l
       ENDDO !n
     ENDDO !m
   ENDDO !l
   gcal=0.5d0*tanh(bigp)+0.5d0
   RETURN
   END SUBROUTINE Fskdco2
